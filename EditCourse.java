    public void setRecordsToTable(){
        try{
            Connection con=DBConnection.getConnection();
            PreparedStatement ps=con.prepareStatement("select * from course");
            ResultSet rs=ps.executeQuery();
            while(rs.next()){
                String courseId=rs.getString("id");
                String courseName=rs.getString("course_name");
                String coursePrice=rs.getString("course_cost");
                
                Object []obj={courseId,courseName,coursePrice};
                
                model=(DefaultTableModel)tbl_coursedetails.getModel();
                model.addRow(obj);
            }
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }
    
    public void clearTable(){
        DefaultTableModel model=(DefaultTableModel)tbl_coursedetails.getModel();
        model.setRowCount(0);
    }
    
    public void addCourse(int id,String courseName,float coursePrice){
        try{
            Connection con=DBConnection.getConnection();
            PreparedStatement ps=con.prepareStatement("insert into course values(?,?,?)");
            ps.setInt(1, id);
            ps.setString(2, courseName);
            ps.setFloat(3, coursePrice);
            int rowCount=ps.executeUpdate();
            if(rowCount==1){
                JOptionPane.showMessageDialog(this,"Course Added Successfully!");
                clearTable();
                setRecordsToTable();
            }
            else{
                JOptionPane.showMessageDialog(this,"Course Insertion Failed!");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this,"Course Insertion Failed!");
            e.printStackTrace();
        }
    }
    
    public void updateCourse(int id, String coursename, float courseprice){
        try{
            Connection con=DBConnection.getConnection();
            PreparedStatement ps=con.prepareStatement("update course set course_name=?,course_cost=? where id=?");
            ps.setString(1, coursename);
            ps.setFloat(2, courseprice);
            ps.setInt(3, id);
            int rowCount=ps.executeUpdate();
            if(rowCount==1){
                JOptionPane.showMessageDialog(this,"Course Updated Successfully!");
                clearTable();
                setRecordsToTable();
            }
            else{
                JOptionPane.showMessageDialog(this,"Course Updation Failed!");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this,"Course Updation Failed!");
            e.printStackTrace();
        }
    }
    
    public void deleteCourse(int id){
        try{
            Connection con=DBConnection.getConnection();
            PreparedStatement ps=con.prepareStatement("delete from course where id=?");
            ps.setInt(1,id);
            int rowCount=ps.executeUpdate();
            if(rowCount==1){
                JOptionPane.showMessageDialog(this,"Course Deleted Successfully!");
                clearTable();
                setRecordsToTable();
            }
            else{
                JOptionPane.showMessageDialog(this,"Course Deletion Failed!");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this,"Course Deletion Failed!");
            e.printStackTrace();
        }
    }
    
    boolean validation(){
        String id=txt_courseid.getText();
        String cname=txt_coursename.getText();
        String cprice=txt_courseprice.getText();
        if(id.equals("")){
            JOptionPane.showMessageDialog(this, "Please enter course id");
            return false;
        }
        if(cname.equals("")){
            JOptionPane.showMessageDialog(this, "Please enter course name");
            return false;
        }
        if(cprice.equals("")){
            JOptionPane.showMessageDialog(this, "Please enter course price");
            return false;
        }
        return true;
    }

