    private void tbl_coursedetailsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_coursedetailsMouseClicked
        int rowNum=tbl_coursedetails.getSelectedRow();
        TableModel model=tbl_coursedetails.getModel();
        txt_courseid.setText(model.getValueAt(rowNum, 0).toString());
        txt_coursename.setText((String)model.getValueAt(rowNum, 1));
        txt_courseprice.setText(model.getValueAt(rowNum, 2).toString());
    }//GEN-LAST:event_tbl_coursedetailsMouseClicked

    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        int id=Integer.parseInt(txt_courseid.getText());
        String coursename=txt_coursename.getText();
        float courseprice=Float.parseFloat(txt_courseprice.getText());
        if(validation()){
            addCourse(id,coursename,courseprice);
        }
    }//GEN-LAST:event_btn_addActionPerformed

